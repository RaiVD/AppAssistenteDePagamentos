package view

class OutputUser {
    fun displayDrawnNameFemale(nome: String): String {
        return "O nome feminino sorteado é: $nome"
    }
    fun displayDrawnNameMale(nome: String): String {
        return "O nome masculino sorteado é: $nome"
    }
    fun displayDrawnNameAnimal(nome: String): String {
        return "O nome de animal sorteado é: $nome"
    }
}